def horizSym(grid):
	grid = grid.split("\n")
	i1 = 1
	while i1 < len(grid):
		i2 = 1
		temp = i1
		while i1 < len(grid) and i1 - i2 > 0 and grid[i1] == grid[i1 - i2]:
			i1 += 1
			i2 += 2
		if i1 == len(grid) or (i1 - i2 == 0 and i1 != i2): return temp
		i1 = temp + 1


